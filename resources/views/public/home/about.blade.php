                I. PRÉSENTATION DE L'ASSOCIATION POUR LA SOLIDARITÉ ET LE DÉVELOPPEMENT DES JEUNES (ASDJ). <br> <br>

                Créée en 2000 à Ouagadougou (Burkina Faso), l'Association pour la Solidarité et le Développement des Jeunes
                (ASDJ) est une association de jeunes. Elle a une vocation panafricaine. <br> <br>

                Depuis plus d'une décennie, elle développe des projets et des programmes dont l'impact sur l'épanouissement
                et le développement des jeunes est manifeste et soutenu par nos efforts inestimables. <br> <br>

                Présente et active dans l'ensemble des 45 provinces réparties dans les 13 régions du Burkina Faso, elle
                existe aussi à travers ses représentants dans quatre des pays de l'espace UEMOA, et dans l'un des pays
                (Tchad) de l'espace CEMAC (Communauté Économique des États de l'Afrique Centrale). <br> <br>

                Notre organisation se veut être un pôle majeur de promotion des droits humains, des peuples et de la
                diversité culturelle. Nos actions consistent à susciter un élan de solidarité et de partage entre les jeunes
                du monde et à promouvoir plus de tolérance, d'esprit de convivialité. <br> <br>

                L'ASDJ a pour mission d'accompagner la jeunesse en vue de son épanouissement et de sa pleine réalisation en
                développant et en soutenant des projets et des programmes à forte participation des jeunes aux questions
                politiques et sociales de leurs localités. <br> <br>

                Connue de la majorité des jeunes à travers certains de nos programmes populaires tels que : SOS Jeunesse
                promo emploi, Programme de Recherche de Bourses et de Stages, sessions régionales de formation en
                renforcement des capacités, etc., notre association ambitionne de conduire des projets plus vastes et plus
                efficaces au profit de la jeunesse. <br> <br>

                L'ASDJ s'est engagée à militer pour l'émergence et la consolidation d'une classe de jeunes leaders qui
                participent à la vie civile de leurs États et au développement de leurs nations. <br> <br>

                Elle est forte d'un effectif de plus de 1000 jeunes militants, de 45 volontaires et de quelques 90
                sympathisants qui la représentent et soutiennent ses efforts de développement sur l'ensemble du territoire
                burkinabé. <br> <br>

                Aussi, nous sommes l'une des structures de jeunesse les mieux organisées et les plus actives. L'esprit
                associatif et activiste qui parvient à motiver le regroupement d'un nombre aussi important de jeunes autour
                d'un idéal de société se retrouve dans le dynamisme qu'ont su insuffler nos premiers responsables dans
                chacun de nos projets, empreints de notre foi en une société civile jeune, participative, active et
                protectrice de ses intérêts. <br> <br>

                La crédibilité de nos actions fait de l'ASDJ un acteur efficace dans la sensibilisation à la citoyenneté et
                au développement de notre pays auprès de la frange jeune de notre pays. <br> <br>

                Nous sommes convaincus que notre expérience de travail, la force que constituent nos effectifs et notre
                présence territoriale sont un tremplin favorable pour une relance de nouveaux partenariats, pour une
                restructuration de nos actions et pour l'initiation avec l'aide d'acteurs intervenant dans la promotion de
                la jeunesse, de projets innovants et plus expressifs de nos ambitions. La production de l'émission
                radiophonique "Championnat national de lecture et d'oralité" en est une parfaite illustration ! <br> <br>
            </p>
